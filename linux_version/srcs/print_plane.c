#include <stdio.h>

void	print_plane(char **world, int limit)
{
	int		idx;

	idx = 0;
	printf("========================================\n");
	while (idx < limit)
	{
		printf("%s\n", world[idx]);
		idx++;
	}
	printf("========================================\n");
}
